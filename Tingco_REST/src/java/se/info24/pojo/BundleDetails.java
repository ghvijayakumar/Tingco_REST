package se.info24.pojo;

import java.util.Date;

/**
 *
 * @author Vijayakumar
 */
public class BundleDetails {

    private String bundleId;
    private String bundleName;
    private String bundleCategoryId;
    private String bundleCategoryName;
    private String bundleTypeId;
    private String bundleTypeName;
    private String groupId;
    private String groupName;
    private Date bundleCreatedDate;

    public String getBundleCategoryId() {
        return bundleCategoryId;
    }

    public void setBundleCategoryId(String bundleCategoryId) {
        this.bundleCategoryId = bundleCategoryId;
    }

    public String getBundleCategoryName() {
        return bundleCategoryName;
    }

    public void setBundleCategoryName(String bundleCategoryName) {
        this.bundleCategoryName = bundleCategoryName;
    }

    public Date getBundleCreatedDate() {
        return bundleCreatedDate;
    }

    public void setBundleCreatedDate(Date bundleCreatedDate) {
        this.bundleCreatedDate = bundleCreatedDate;
    }

    public String getBundleId() {
        return bundleId;
    }

    public void setBundleId(String bundleId) {
        this.bundleId = bundleId;
    }

    public String getBundleName() {
        return bundleName;
    }

    public void setBundleName(String bundleName) {
        this.bundleName = bundleName;
    }

    public String getBundleTypeId() {
        return bundleTypeId;
    }

    public void setBundleTypeId(String bundleTypeId) {
        this.bundleTypeId = bundleTypeId;
    }

    public String getBundleTypeName() {
        return bundleTypeName;
    }

    public void setBundleTypeName(String bundleTypeName) {
        this.bundleTypeName = bundleTypeName;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }


}
